/*
** strcpy -- copy strings
** strcpy() functions copy the string src to dst
** (including the terminating `\0' character.
*/

#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = src[i];
	return (dst);
}

